      diamond_bottom: "\\",
      diamond_left: "/",
      diamond_right: "\\",
      circle_endpoint: "o",
      cross_endpoint: "x",
      sg_top_left: "+",
      sg_top_right: "+",
      sg_bottom_left: "+",
      sg_bottom_right: "+",
      sg_horizontal: "-",
      sg_vertical: "|"
    }
  end
end
